<template>
  <div>
    <button
      v-if="buttonData.link_to_details"
      :style="{
        top: `${buttonData.position.top}%`,
        left: `${buttonData.position.left}%`
      }"
      @click="$emit('openDetail')"
      class="navigation-button"
    >
      <svg-icon :name="buttonData.icon" />
    </button>
    <nuxt-link
      v-else
      :to="localePath(buttonPath)"
      :style="{
        top: `${buttonData.position.top}%`,
        left: `${buttonData.position.left}%`
      }"
      @click.native="showPlaceholder"
      class="navigation-button"
    >
      <svg-icon :name="buttonData.icon" />
    </nuxt-link>
  </div>
</template>

<script>
export default {
  props: {
    buttonData: {
      type: Object,
      default: null
    }
  },
  computed: {
    buttonPath() {
      const slug = this.buttonData.related_item.post_name
      const parentSlug = this.buttonData.related_item.parent_slug
      const exhibition = this.$route.params.exhibition

      if (parentSlug) {
        return {
          name: 'exhibition-parent-child',
          params: {
            parent: parentSlug,
            child: slug,
            exhibition
          }
        }
      } else {
        return {
          name: 'exhibition-parent',
          params: {
            parent: slug,
            exhibition
          }
        }
      }
    }
    // modalData() {
    //   const detailedView = this.$store.getters.getSingleRoom(
    //     this.buttonData.related_item.post_name
    //   )

    //   return detailedView &&
    //     detailedView.acf &&
    //     detailedView.acf.detailed_view_content
    //     ? detailedView.acf.detailed_view_content
    //     : null
    // }
  },
  methods: {
    showPlaceholder() {
      const room = this.$store.getters.getSingleRoom(
        this.buttonData.related_item.post_name
      )

      if (room.featured_image && room.featured_image.sizes.medium) {
        this.$store.commit(
          'SetPlaceholderImage',
          room.featured_image.sizes.large
        )
      }
    }
  }
}
</script>

